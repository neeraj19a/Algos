package MakeJSON;

public class CombineAllJsonObjectsPersonalPassportCommunicationDetails {

    private PersonalDetails personalDetails;
    private PassportDetails passportDetails;
    private CommunicationDetails communicationDetails;

    public PersonalDetails getPersonalDetails() {
        return personalDetails;
    }

    public void setPersonalDetails(PersonalDetails personalDetails) {
        this.personalDetails = personalDetails;
    }

    public PassportDetails getPassportDetails() {
        return passportDetails;
    }

    public void setPassportDetails(PassportDetails passportDetails) {
        this.passportDetails = passportDetails;
    }

    public CommunicationDetails getCommunicationDetails() {
        return communicationDetails;
    }

    public void setCommunicationDetails(CommunicationDetails communicationDetails) {
        this.communicationDetails = communicationDetails;
    }

}

